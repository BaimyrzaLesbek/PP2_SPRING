def draw_line():
    for i in range(0, 40):
        pygame.draw.line(screen, (255,255,255), (i*tile_size, 0), (i*tile_size, HEIGHT))
    for i in range(0, 30):
        pygame.draw.line(screen, (255,255,255), (0, i*tile_size), (WIDTH, i*tile_size))

def draw_world():
    for row in enumerate(world_data):
        for column in enumerate(row[1]):
            if column[1] == 1:
                img = pygame.image.load("./images/brick-wall.png")
                image = pygame.transform.scale(img, (20,20))
                screen.blit(image, (column[0]*tile_size, row[0]*tile_size))

run = True
while run:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            run = False
        if event.type == pygame.MOUSEBUTTONUP:
            position = pygame.mouse.get_pos()
            x = position[0]//20
            y = position[1]//20
            if x<40 and y<30:
                world_data[y][x] += 1
                if world_data[y][x] == 2:
                    world_data[y][x] = 0

    screen.fill((0,204,255))
    draw_line()
    draw_world()
    pygame.display.flip()
